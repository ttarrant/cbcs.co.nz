(function($) {

  "use strict";


  //Hide Loading Box (Preloader)
  function handlePreloader() {
    if($('.preloader').length){
      $('.preloader').delay(500).fadeOut(500);
    }
  }


  //Update Header Style + Scroll to Top
  function headerStyle() {
    if($('.main-header').length){
      var topHeader = $('.header-top').innerHeight();
      var windowpos = $(window).scrollTop();
      if (windowpos >= topHeader) {
        $('.main-header').addClass('fixed-header');
        $('.scroll-to-top').fadeIn(300);
      } else {
        $('.main-header').removeClass('fixed-header');
        $('.scroll-to-top').fadeOut(300);
      }
    }
  }

  //Search Box Hide / Show
  if($('.header-lower .search-btn').length){
    $('.header-lower .search-btn').on('click', function() {
      $('.search-box.toggle-box').slideToggle(500);
      // animate
        $('html, body').animate({
         scrollTop: $('html, body').offset().top
       }, 1000);
    });
  }

  //Submenu Dropdown Toggle
  if($('.main-header li.dropdown ul').length){
    $('.main-header li.dropdown').append('<div class="dropdown-btn"></div>');

    //Dropdown Button
    $('.main-header li.dropdown .dropdown-btn').on('click', function() {
      $(this).prev('ul').slideToggle(500);
    });
  }


  //Main Slider
  if($('.main-slider .tp-banner').length){

    jQuery('.main-slider .tp-banner').show().revolution({
      delay:10000,
      startwidth:1200,
      startheight:620,
      hideThumbs:600,

      thumbWidth:80,
      thumbHeight:50,
      thumbAmount:5,

      navigationType:"bullet",
      navigationArrows:"0",
      navigationStyle:"preview4",

      touchenabled:"on",
      onHoverStop:"off",

      swipe_velocity: 0.7,
      swipe_min_touches: 1,
      swipe_max_touches: 1,
      drag_block_vertical: false,

      parallax:"mouse",
      parallaxBgFreeze:"on",
      parallaxLevels:[7,4,3,2,5,4,3,2,1,0],

      keyboardNavigation:"off",

      navigationHAlign:"center",
      navigationVAlign:"bottom",
      navigationHOffset:0,
      navigationVOffset:20,

      soloArrowLeftHalign:"left",
      soloArrowLeftValign:"center",
      soloArrowLeftHOffset:20,
      soloArrowLeftVOffset:0,

      soloArrowRightHalign:"right",
      soloArrowRightValign:"center",
      soloArrowRightHOffset:20,
      soloArrowRightVOffset:0,

      shadow:0,
      fullWidth:"on",
      fullScreen:"off",

      spinner:"spinner4",

      stopLoop:"off",
      stopAfterLoops:-1,
      stopAtSlide:-1,

      shuffle:"off",

      autoHeight:"off",
      forceFullWidth:"on",

      hideThumbsOnMobile:"on",
      hideNavDelayOnMobile:1500,
      hideBulletsOnMobile:"on",
      hideArrowsOnMobile:"on",
      hideThumbsUnderResolution:0,

      hideSliderAtLimit:0,
      hideCaptionAtLimit:0,
      hideAllCaptionAtLilmit:0,
      startWithSlide:0,
      videoJsPath:"",
      fullScreenOffsetContainer: ".main-slider"
    });


  }



  //Sponsors Slider
  if ($('.sponsors-section .slider').length) {
    $('.sponsors-section .slider').owlCarousel({
      loop:true,
      margin:20,
      nav:true,
      autoplay: 5000,
      responsive:{
        0:{
          items:1
        },
        600:{
          items:2
        },
        800:{
          items:3
        },
        1024:{
          items:4
        },
        1100:{
          items:5
        }
      }
    });
  }

  //Adjust Footer Background
  function footerStyle() {
    if($('.main-footer .contact-widget').length){
      var contactWidth = $('.main-footer .contact-widget').innerWidth();
      //var windowWidth = $(window).width();
      var contWidth = $('.main-footer .auto-container').width();
      $('.main-footer .footer-bg-layer').css({'width':contWidth-contactWidth+15});
    }
  }

  //Date TimePicker
  if($('.date-field').length) {
    $('.date-field').datepick();
  }

  //Tabs Box
  if($('.tabs-section').length){
    $('.tabs-section .tab-btn').on('click', function(e) {
      e.preventDefault();
      var target = $($(this).attr('href'));
      $('.tabs-section .tab-btn').removeClass('active');
      $(this).addClass('active');
      $('.tabs-section .tab').fadeOut(0);
      $('.tabs-section .tab').removeClass('active-tab');
      $(target).fadeIn(300);
      $(target).addClass('active-tab');
      var windowWidth = $(window).width();
      if (windowWidth <= 700) {
        $('html, body').animate({
           scrollTop: $('.tabs-section .content-column').offset().top-100
         }, 1000);
      }
    });

  }


  //Three Column Slider
  if ($('.column-carousel.three-column').length) {
    $('.column-carousel.three-column').owlCarousel({
      loop:true,
      margin:30,
      nav:true,
      autoplayHoverPause:false,
      autoplay: 5000,
      smartSpeed: 700,
      responsive:{
        0:{
          items:1
        },
        600:{
          items:2
        },
        800:{
          items:2
        },
        1024:{
          items:3
        },
        1100:{
          items:3
        }
      }
    });
  }

  //Skill Progress Bar
  if($('.skill-box .bar-fill').length){
    $(".skill-box .bar-fill").each(function() {
      var progressWidth = $(this).attr('data-percent');
      $(this).css('width',progressWidth+'%');
      $(this).parents('.bar').children('.percent').html(progressWidth+'%');
    });
  }


  //Filters Section / Mixitup
  if($('.filter-list').length){
    $('.filter-list').mixitup({});
  }

  // Fact Counter
  function factCounter() {
    if($('.fact-counter').length){
      $('.fact-counter .column.animated').each(function() {

        var $t = $(this),
          n = $t.find(".count-text").attr("data-stop"),
          r = parseInt($t.find(".count-text").attr("data-speed"), 10);

        if (!$t.hasClass("counted")) {
          $t.addClass("counted");
          $({
            countNum: $t.find(".count-text").text()
          }).animate({
            countNum: n
          }, {
            duration: r,
            easing: "linear",
            step: function() {
              $t.find(".count-text").text(Math.floor(this.countNum));
            },
            complete: function() {
              $t.find(".count-text").text(this.countNum);
            }
          });
        }

      });
    }
  }


  //Accordions
  if($('.accordion-box').length){
    $('.accordion-box .acc-btn').on('click', function() {
    if($(this).hasClass('active')!==true){
        $('.accordion-box .acc-btn').removeClass('active');
      }

    if ($(this).next('.acc-content').is(':visible')){
        $(this).removeClass('active');
        $(this).next('.acc-content').slideUp(500);
      }
    else{
        $(this).addClass('active');
        $('.accordion-box .acc-content').slideUp(500);
        $(this).next('.acc-content').slideDown(500);
      }
    });
  }


  //Image Carousel Slider
  if ($('.image-carousel .slider').length) {
    $('.image-carousel .slider').owlCarousel({
      loop:true,
        navigation : false,
        slideSpeed : 500,
        responsive:{
        0:{
          items:1
        },
        600:{
          items:1
        },
        1024:{
          items:1
        },
        1200:{
          items:1
        },
        1400:{
          items:1
        }
      }
    });
  }

  //LightBox / Fancybox
  if($('.lightbox-image').length) {
    $('.lightbox-image').fancybox();
  }


  //Contact Form Validation
  if($('#contact-form').length){
    $('#contact-form').validate({
      rules: {
        username: {
          required: true
        },
        email: {
          required: true,
          email: true
        },
        subject: {
          required: true
        },
        message: {
          required: true
        }
      }
    });
  }


  // Google Map Settings
  if($('#map-location').length){
    var map;
     map = new GMaps({
      el: '#map-location',
      zoom: 14,
      scrollwheel:false,
      //Set Latitude and Longitude Here
      lat: -37.817085,
      lng: 144.955631
      });

      //Add map Marker
      map.addMarker({
      lat: -37.817085,
      lng: 144.955631,
      infoWindow: {
        content: '<p style="text-align:center;"><strong>Envato</strong><br>Melbourne VIC 3000, Australia</p>'
      }

    });
  }


  // Scroll to top
  if($('.scroll-to-top').length){
    $(".scroll-to-top").on('click', function() {
       // animate
       $('html, body').animate({
         scrollTop: $('html, body').offset().top
       }, 1000);

    });
  }


  // Elements Animation
  if($('.wow').length){
    var wow = new WOW(
      {
      boxClass:     'wow',      // animated element css class (default is wow)
      animateClass: 'animated', // animation css class (default is animated)
      offset:       0,          // distance to the element when triggering the animation (default is 0)
      mobile:       true,       // trigger animations on mobile devices (default is true)
      live:         true       // act on asynchronously loaded content (default is true)
      }
    );
    wow.init();
  }

/* ==========================================================================
   When document is ready, do
   ========================================================================== */

  $(document).on('ready', function() {
    headerStyle();
    footerStyle();
  });

/* ==========================================================================
   When document is Scrollig, do
   ========================================================================== */

  $(window).on('scroll', function() {
    headerStyle();
    factCounter();
  });

/* ==========================================================================
   When document is loading, do
   ========================================================================== */

  $(window).on('load', function() {
    handlePreloader();
  });


/* ==========================================================================
   When Window is resizing, do
   ========================================================================== */

  $(window).on('resize', function() {
    footerStyle();
  });


})(window.jQuery);
